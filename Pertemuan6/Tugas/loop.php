<?php                               // pembuka php
for ($x = 0; $x <= 10; $x++) {      // perulangan for yaitu perulangan yang ditentukan kondisinya baru di cetak
  echo "The number is: $x <br>";
}

$x = 1;

do {                                // perulangan do while adalah perulangan yang dicetak terlebih dahulu setelah itu perulangan dimulai
  echo "The number is: $x <br>";            
  $x++;
} while ($x <= 5);
?>                                  <!-- penutup php -->